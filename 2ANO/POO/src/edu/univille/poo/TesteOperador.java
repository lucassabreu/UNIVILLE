package edu.univille.poo;

public class TesteOperador {
	public static void main(String[] args) {
		int i = 10;

		System.out.println("Valor i: " + i);
		// operador de incremento POS fixado
		System.out.println("Valor i: " + i++);
		// operador de incremento PRE fixado
		System.out.println("Valor i: " + ++i);
	}
}
